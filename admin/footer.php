<h2>Postup updatu:</h2><p>
<div class="help">

1. v select session vyberes co chces aktualizovat (schedule, standings, <i>stats</i>, data – news)<br>
2. v schedules je ulozeny subor tabs.rar na vypocet tabuliek a rozpisu<p>
3. rozpakuj tabs.zip, mas tabs.xls, otvor, daj povolit makra (ak mas makra celkovo zakazane, tak  chod cez menu tools (nastroje) - macro - security - vyber stredne zabezpecenie):<p>
3.a. do listu <b>data</b> zapis vysledky aktualneho kola (pozor zmenu domaceho timu), <br>
3.b. klikni na <b>calculate</b> v pravo hore, prebehne vypocet tabuliek<p>

<b>update rozpisu zapasov</b><br>
4. pravym klikni na scheduleXX.txt (playoffXX.txt pre playoff), uloz na disk, otvor v Editplus. Z data v tab.xls skopiruj B3 : P197, vloz do editplus, daj ctrl+H, pri find what mas na konci maly rozklikavac, vyber v nom Tab character, Replace with | (zvysla ciara, nachadza sa na anglickej klavesnici niekde nad \), uloz do scheduleXX.txt a nahraj na web cez session schedule <p>

<b>update tabuliek</b><br>
4. pravym klikni na standingsXX.txt, uloz na disk, otvor v Editplus. Z out v tab.xls skopiruj N46 : Z77, vloz do editplus, daj ctrl+H, pri find what mas na konci maly rozklikavac, vyber v nom Tab character, Replace with | (zvysla ciara, nachadza sa na anglickej klavesnici niekde nad \), uloz do standingsXX.txt a nahraj na web cez session schedule <p>

5. uloz tabs.xls, spakuj do rar a nahraj v session schedules cez browse hore na tejto stranke...<p>

6. pre info, keby niekedy bolo treba napisat nejaku novinku, tak v session data, je subor news.txt, ked ho otvoris v editplus, tak do prveho riadku hore sa pise najnovsia novinka v tvare nadpis|datum|text. Ked to ulozis na web cez session data, tak sa automaticky zobrazi nova novinka na stranke<p>

<b>Hviezdy tyzdna</b><br />
7. v session vyber stars, je tam subor starXX.txt, do prveho riadku sa pisu aktualne hviezdy v tvare:<br />
datumy|meno 1. hviezda|skratka timu|stats|id hraca|meno 2. hviezda|skratka timu|stats|id hraca|meno 3. hviezda|skratka timu|stats|id hraca| <br />
treba sa pozriet na predchadzajuce tyzdne.<br />
uloz a upload do session stars<p>


